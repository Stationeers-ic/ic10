		expect(runFunc(instructions.sll, ["r0", -0x10000000000000, 1])).resolves.toBe(-0x20000000000000)
		expect(runFunc(instructions.sll, ["r0", 1, 53])).resolves.toBe(-0x20000000000000)
		expect(runFunc(instructions.sll, ["r0", 1, 54])).resolves.toBe(0)
